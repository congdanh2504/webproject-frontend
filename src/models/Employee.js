export function Employee(
  avatar,
  firstName,
  lastName,
  dob,
  address,
  province,
  mobile,
  CV,
) {
  this.firstName = firstName;
  this.avatar = avatar;
  this.lastName = lastName;
  this.dob = dob;
  this.address = address;
  this.province = province;
  this.mobile = mobile;
  this.CV = CV;
}
